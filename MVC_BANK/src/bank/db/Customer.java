package bank.db;

public class Customer 
{
	private int actno;
	private double amount;
	
	public int getActno() 
	{
		return actno;
	}
	public void setActno(int actno) 
	{
		this.actno = actno;
	}
	public double getAmount() 
	{
		return amount;
	}
	public void setAmount(double amount) 
	{
		this.amount = amount;
	}
	
	
}
